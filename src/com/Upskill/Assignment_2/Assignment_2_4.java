//4.  Write a for loop Upto 500 which will increase the value double every time

public class Assignment_2_4 {
	
	public static void main(String[] args) {
		
		
		for(int i = 1; i <= 500; i*=2) {
			
			System.out.println("Double Value: " + i);
			
		}
	}
}  



 
 